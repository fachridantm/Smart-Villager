    private void btnSeninMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSeninMouseClicked
        // TODO add your handling code here:
        Tiket t = new Tiket();

        WaktuDaftar = LocalDateTime.now();
        dtf = DateTimeFormatter.ofPattern("EEEE, dd MMMM yyyy HH:mm");

        t.show();
        t.pnlTiketKTP.setVisible(true);
        t.pnlTiketKK.setVisible(false);

        t.lblNamaA.setText(this.lblNama.getText());
        t.lblNIKA.setText(this.lblNIK.getText());
        t.lblWaktuDaftarKTP.setText(WaktuDaftar.format(dtf));
        t.lblWaktuBuatKTP.setText("Senin di pekan berikutnya setelah pendaftaran");

        boolean check = isFull(tailSenin);
        if (check) {
            JOptionPane.showMessageDialog(null, "ANTRIAN PENUH! Silakan pilih hari lain!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            tailSenin++;
            dataSenin[tailSenin] = this.lblNama.getText();
            JOptionPane.showMessageDialog(null, "Data telah dimasukkan ke dalam antrian!", "Information", JOptionPane.INFORMATION_MESSAGE);
            listArray.setListData(dataSenin);
        }
    }//GEN-LAST:event_btnSeninMouseClicked

