    private void btnSubmitOperatorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitOperatorMouseClicked
        // TODO add your handling code here:
        if (txtOperator.getText().equalsIgnoreCase("Admin") && pwOperator.getText().equals("kelompok3")) {

            pnlLoaderOperator.show();
            pnlLogin.hide();

            lblOP.setText(this.txtOperator.getText().toUpperCase());
            cmbNamaHari.setSelectedIndex(0);

            new java.util.Timer().schedule(new TimerTask() {

                @Override
                public void run() {

                    pnlMenuOperator.show();

                    pnlLoaderOperator.hide();

                }
            }, 1000 * 3);

        } else {
            JOptionPane.showMessageDialog(null, "Username atau Password salah! Silakan input kembali!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitOperatorMouseClicked

